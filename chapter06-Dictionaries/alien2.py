# Starting with an empty dictionary.
alien_0 = {}

alien_0['color'] = 'green'
alien_0['points'] = 5

print(alien_0)

# Modifying values in a dictionary

print("The alien is {}.".format(alien_0['color']))
print("\nNow to change the color!")

alien_0['color'] = 'yellow'

print("The alien is now {}.".format(alien_0['color']))
